import pdfMake from 'pdfmake/build/pdfmake.js';
import pdfFonts from 'pdfmake/build/vfs_fonts.js';
import htmlToPdfmake from 'html-to-pdfmake';
import { JSDOM } from 'jsdom';
import HtmlToDocx from '@turbodocx/html-to-docx';
import axios from 'axios';
import { marked } from 'marked';
import { asyncHandler } from '../utils/errorHandler.js';

// Fix for ES modules: set vfs fonts
pdfMake.vfs = pdfFonts.vfs || pdfFonts.default?.vfs || pdfFonts.pdfMake?.vfs || pdfFonts;

const slugifyTitle = (title = 'blog') =>
  title
    .toString()
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '') || 'blog';

// Helper: Escape XML special characters to prevent XML parsing crashes in Word generation
function escapeXml(unsafe) {
  if (typeof unsafe !== 'string') return unsafe;
  return unsafe.replace(/[<>&'"]/g, (c) => {
    switch (c) {
      case '<': return '&lt;';
      case '>': return '&gt;';
      case '&': return '&amp;';
      case '\'': return '&apos;';
      case '"': return '&quot;';
      default: return c;
    }
  });
}

// Helper: Convert image URL to base64 dataURL
async function imageUrlToBase64(url) {
  try {
    const response = await axios.get(url, {
      responseType: 'arraybuffer',
      timeout: 10000,
      headers: {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
      }
    });
    
    const contentType = response.headers['content-type'] || 'image/jpeg';
    
    // pdfmake/pdfkit only support PNG and JPEG/JPG. WebP, SVG, GIF etc. will crash pdfmake.
    const isSupported = contentType.includes('jpeg') || contentType.includes('jpg') || contentType.includes('png');
    if (!isSupported) {
      console.warn(`Unsupported image format: ${contentType} for URL: ${url}`);
      return null;
    }

    const base64 = Buffer.from(response.data).toString('base64');
    return `data:${contentType};base64,${base64}`;
  } catch (error) {
    console.warn(`Failed to convert image to base64: ${url}`, error.message);
    return null; // Return null so pdfmake/html-to-docx skips this image or we replace it
  }
}

// Helper: Replace all image URLs in HTML with base64
async function convertImagesToBase64(html) {
  if (!html) return html;
  
  const imgRegex = /<img[^>]*src=["']([^"']+)["'][^>]*>/gi;
  const matches = [...html.matchAll(imgRegex)];
  
  let processedHtml = html;
  
  for (const match of matches) {
    const originalTag = match[0];
    const imageUrl = match[1];
    
    // Skip if already base64
    if (imageUrl.startsWith('data:')) continue;
    
    const base64Url = await imageUrlToBase64(imageUrl);
    
    if (base64Url) {
      processedHtml = processedHtml.replace(originalTag, originalTag.replace(imageUrl, base64Url));
    } else {
      // Replace failed image with a placeholder text to avoid pdfmake crash
      processedHtml = processedHtml.replace(
        originalTag, 
        '<p style="color:#999;font-style:italic;text-align:center;">[Image unavailable]</p>'
      );
    }
  }
  
  return processedHtml;
}

// Helper: Prepare HTML content by converting markdown (if necessary) and appending missing images
function prepareHtmlContent(content, title, images = []) {
  const looksLikeHTML = /<\w+[^>]*>/.test(content || '');
  let html = looksLikeHTML ? content : marked.parse(content || '');

  // Append images if not already embedded
  const hasImg = /<img\s/i.test(html || '');
  if (!hasImg && Array.isArray(images) && images.length > 0) {
    const imagesBlock = images
      .map((img, i) => `<p style="text-align: center;"><img src="${img.url}" alt="${img.alt || `Image ${i + 1}`}" /></p>`)
      .join('\n');
    html += `<hr /><div>${imagesBlock}</div>`;
  }
  return html;
}

// Helper: Convert HTML to PDF using html-to-pdfmake
async function generatePDF(title, htmlContent) {
  const { window } = new JSDOM('');
  
  // Convert HTML to pdfmake format
  const pdfContent = htmlToPdfmake(htmlContent, { window });
  
  const docDefinition = {
    content: [
      { text: title, style: 'title' },
      { text: 'Generated by AI Blog Generator', style: 'subheader' },
      { canvas: [{ type: 'line', x1: 0, y1: 5, x2: 515, y2: 5, lineWidth: 1, lineColor: '#cccccc' }] },
      { text: '\n' },
      ...pdfContent,
    ],
    styles: {
      title: { fontSize: 24, bold: true, color: '#111111', margin: [0, 0, 0, 10] },
      subheader: { fontSize: 10, color: '#666666', margin: [0, 0, 0, 20] },
    },
    defaultStyle: { fontSize: 11, lineHeight: 1.4 },
  };

  const pdfDoc = pdfMake.createPdf(docDefinition);
  return await pdfDoc.getBuffer();
}

// Helper: Convert HTML to DOCX using html-to-docx
async function generateDOCX(title, htmlContent) {
  const escapedTitle = escapeXml(title);
  const fullHtml = `
    <!DOCTYPE html>
    <html>
    <head>
      <meta charset="UTF-8">
      <title>${escapedTitle}</title>
      <style>
        body { font-family: Arial, sans-serif; padding: 40px; line-height: 1.6; }
        h1 { color: #111; font-size: 28px; }
        h2 { color: #222; font-size: 22px; margin-top: 30px; }
        img { max-width: 100%; height: auto; }
      </style>
    </head>
    <body>
      <h1>${escapedTitle}</h1>
      ${htmlContent}
    </body>
    </html>
  `;

  const docx = await HtmlToDocx(fullHtml, null, {
    title: escapedTitle,
    creator: 'AI Blog Generator',
    margins: { top: 1440, right: 1440, bottom: 1440, left: 1440 },
    pageNumber: true,
  });

  return Buffer.from(docx);
}

// ========== EXPORT CONTROLLERS ==========

export const exportToPDFFile = asyncHandler(async (req, res) => {
  const { title = 'Blog', content = '', images = [] } = req.body;
  const rawHtml = prepareHtmlContent(content, title, images);
  const html = await convertImagesToBase64(rawHtml);
  const pdfBuffer = await generatePDF(title, html);

  res.setHeader('Content-Type', 'application/pdf');
  res.setHeader('Content-Disposition', `attachment; filename="${slugifyTitle(title)}.pdf"`);
  res.send(pdfBuffer);
});

export const exportToDOCXFile = asyncHandler(async (req, res) => {
  const { title = 'Blog', content = '', images = [] } = req.body;
  const rawHtml = prepareHtmlContent(content, title, images);
  const html = await convertImagesToBase64(rawHtml);
  const docxBuffer = await generateDOCX(title, html);

  res.setHeader('Content-Type', 'application/vnd.openxmlformats-officedocument.wordprocessingml.document');
  res.setHeader('Content-Disposition', `attachment; filename="${slugifyTitle(title)}.docx"`);
  res.send(docxBuffer);
});

// Aliases to support multiple import preferences
export const exportToPDF = exportToPDFFile;
export const exportToDOCX = exportToDOCXFile;
